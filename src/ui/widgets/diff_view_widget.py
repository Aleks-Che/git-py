"""Colour-coded diff view with line-number gutter.

Replaces the plain ``QPlainTextEdit`` that was used through Stage 3-8 as
a temporary diff viewer. Uses :func:`src.core.diff_parser.parse_diff_lines`
to classify each line and paints backgrounds accordingly:

* Additions  — green-tinted background
* Deletions  — red-tinted background
* Hunk headers (``@@ … @@``) — cyan-tinted background
* Context / empty lines — transparent (the editor background shows through)

A gutter on the left draws the **file** line number for each change —
additions and context use the new-file line, deletions use the
old-file line, both derived from the parsed hunk header. The user
sees "this addition lives on line 42 of the file" rather than a
sequential 1, 2, 3 … diff index.

File-level header lines (``diff --git``, ``index``, ``--- a/…``,
``+++ b/…``, ``old mode``, etc.) are not displayed: the viewer is
opened from a specific file, so the file header is redundant noise.

The widget is read-only and has no file-io or Git logic — it only
receives a diff string via :meth:`set_diff`.

View modes
----------
The top of the widget carries a small centred toolbar with two checkable
buttons that toggle between **Changes only** and **Full document**:

* **Changes only** (default) — the diff as generated by ``git diff`` with
  ``context_lines=3``, just like every prior version. Best when the user
  only wants to see what changed.
* **Full document** — the same file rendered with effectively unlimited
  context so every line of both versions is present. The additions,
  deletions and hunks appear in the same colour-coded form as in the
  changes-only view; only the unchanged context expands to cover the
  whole file. Switching to this mode (or loading a new diff in this
  mode) auto-scrolls to the first change so the user lands on it
  without hunting through the file head.

The widget stores both variants of the diff text and renders whichever
matches the active mode, so toggling is instant (no re-diff needed).
Upstream code is expected to call :meth:`set_diff_pair` (or the legacy
:meth:`set_diff` + :meth:`set_full_document_diff`) when a new file is
selected; the toolbar then decides which variant to show.

Custom scrollbar
----------------
The vertical scrollbar is a :class:`_DiffScrollBar` that paints as a
minimap of the diff:

* Wider than the platform default so a thin vertical line in the
  middle can split it into two halves.
* **Left half** — red tick mark for every deletion, positioned by
  its file line number.
* **Right half** — green tick mark for every addition, positioned
  the same way.
* Background and thumb use alpha-blended colours so the bar reads
  as semi-transparent without breaking hit-testing.
"""
